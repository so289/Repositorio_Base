# Utilize um loop while e um loop for para contar de 0 até o número que o usuário digitar:

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

# LOOP WHILE
numero =int(input ('Digite um número para contar :'))
a = 0

while a<=numero:
    print(a)
    a= a+1

print()


# LOOP FOR
numero =int(input ('Digite um número para contar :'))
a = 0


for a in range (0,numero):
    print(a+1)



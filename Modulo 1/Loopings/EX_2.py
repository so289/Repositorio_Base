# Utilize um loop while e um loop for para percorrer a lista e exibir os valores dos items contidos na lista

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

frutas = ['maçã', 'banana', 'morango', 'uva', 'laranja']

# LOOP WHILE 
cont=0

while cont<len(frutas):
    print(frutas[cont])
    cont= cont+1

print()
# LOOP FOR
cont = 0
for fruta in frutas:
    print(fruta)
    cont = cont +1
    


    



